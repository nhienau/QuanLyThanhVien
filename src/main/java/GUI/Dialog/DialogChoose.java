    public DialogChoose(java.awt.Frame parent, boolean modal, String description, Callback callback) {
        super(parent, modal);
        initComponents();
        lblDescription.setText(description);

        this.tableModel = new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Tên"
                }
        ) {
            boolean[] canEdit = new boolean[]{
                false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };
        table.setModel(this.tableModel);

        setLocationRelativeTo(parent);
        this.callback = callback;
        this.data = new ArrayList<>();
        this.query = "";

        this.data = this.callback.getList(query);
        displayList(this.query, this.data);
    }

    private void displayList(String query, ArrayList<String> list) {
        tableModel.setRowCount(0);
        if (query.isEmpty()) {
            tableModel.addRow(new Object[]{"Tất cả"});
        }
        for (String s : list) {
            Object[] row = {s};
            tableModel.addRow(row);
        }
    }

    public static abstract class Callback {

        public Callback() {
        }

        public abstract ArrayList<String> getList(String query);

        public abstract void handleConfirm(String selectedValue);
    }

