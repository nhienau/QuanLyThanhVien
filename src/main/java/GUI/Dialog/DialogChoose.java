    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if (txtInput.getText().trim().toLowerCase().equals(query.toLowerCase())) {
            return;
        }
        query = txtInput.getText().trim();
        data = callback.getList(query);
        displayList(query, data);
    }//GEN-LAST:event_btnSearchActionPerformed

    private void txtInputKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtInputKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtInput.getText().trim().toLowerCase().equals(query.toLowerCase())) {
                return;
            }
            query = txtInput.getText().trim();
            data = callback.getList(query);
            displayList(query, data);
        }
    }//GEN-LAST:event_txtInputKeyPressed

    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:+
        int row = table.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn");
            return;
        }
        String selectedValue = table.getModel().getValueAt(row, 0).toString();
        String value = selectedValue.equalsIgnoreCase("Tất cả") ? "" : selectedValue;
        this.callback.handleConfirm(value);
        this.dispose();
    }//GEN-LAST:event_btnConfirmActionPerformed

